#!/usr/bin/env bash
# PreToolUse: block edits to generated artifacts that get rewritten by tooling.
set -euo pipefail

input=$(cat)
file_path=$(echo "$input" | jq -r '.tool_input.file_path // empty')
[[ -z "$file_path" ]] && exit 0

case "$file_path" in
    *.xcodeproj/*|*.xcworkspace/*|*/apps/ios/Derived/*|*/apps/ios/.derived-data/*|*/apps/ios/.build/*)
        echo "Blocked: '$file_path' is generated by Tuist. Edit apps/ios/Project.swift instead and the project will regenerate." >&2
        exit 2
        ;;
    */services/convex/_generated/*|*/convex/_generated/*)
        echo "Blocked: '$file_path' is generated by 'convex codegen'. Edit services/convex/schema.ts (or the function files) and the types regenerate." >&2
        exit 2
        ;;
    */node_modules/*)
        echo "Blocked: '$file_path' is inside node_modules. Edit the source package or update package.json." >&2
        exit 2
        ;;
    */dist/*|*/.output/*|*/.nitro/*|*/.tanstack/*|*/.wrangler/*)
        echo "Blocked: '$file_path' is a build output. Edit the source under src/ and rebuild." >&2
        exit 2
        ;;
    */docs/.vitepress/cache/*|*/docs/.vitepress/dist/*)
        echo "Blocked: '$file_path' is VitePress build output. Edit the source markdown or theme files instead." >&2
        exit 2
        ;;
    */build/*)
        # Catch generic build dirs but allow apps/web/build.ts and similar
        if [[ "$file_path" == */build/* && ! "$file_path" == */build.ts && ! "$file_path" == */build.js ]]; then
            echo "Blocked: '$file_path' is inside a build/ output directory." >&2
            exit 2
        fi
        ;;
esac

exit 0
